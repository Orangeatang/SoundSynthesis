#include "stdafx.h"
#include "IObject.h"


//////////////////////////////////////////////////////////////////////////
/// IObject
//////////////////////////////////////////////////////////////////////////

IObject::IObject()
{
    UuidCreate( &myId );
}

//////////////////////////////////////////////////////////////////////////

IObject::~IObject()
{
}

//////////////////////////////////////////////////////////////////////////

const UUID& IObject::GetId() const
{
    return myId;
}

//////////////////////////////////////////////////////////////////////////

bool IObject::operator==( const UUID& anId ) const
{
    return (anId == myId);
}

//////////////////////////////////////////////////////////////////////////

bool IObject::operator==( const IObject& anObject ) const
{
    return (anObject.myId == myId);
}

//////////////////////////////////////////////////////////////////////////

bool IObject::operator==( const IObject* anObject ) const
{
    if( anObject == nullptr )
    {
        return false;
    }

    return (anObject->myId == myId);
}

